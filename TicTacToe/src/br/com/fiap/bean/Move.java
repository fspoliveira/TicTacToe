package br.com.fiap.bean;

public class Move {
	
	private String move;
	private String message;
	
	public Move(){}

	public String getMove() {
		return move;
	}

	public void setMove(String move) {
		this.move = move;
	}

	public String getMessage() {
		return message;
	}

	public void setMessage(String message) {
		this.message = message;
	}
	

}
